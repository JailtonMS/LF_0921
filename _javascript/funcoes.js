function Verificar(){
    var somanum = 0
    var Concursos = 0
    var pontos = 0
    var verfJogo = 0
    var ponto11 = 0
    var ponto12 = 0
    var ponto13 = 0
    var ponto14 = 0
    var ponto15 = 0
    var Lin = 0
    var Col = 0
    var confjogo = document.getElementById("Confere")
    var somatotal = document.getElementById("soma")

    if (valores.length == 0 || valores.length > 15) {
        if (valores.length == 0)
            window.alert('Selecione os números antes de de cálcular!')
        if (valores.length > 15)
            window.alert('Selecione no máximo 15 números')
    }else {
        /*window.alert('Teste calculando!!!')*/
        for (var pos in valores){
            somanum += valores[pos]
        }
        somatotal.style.color="#00f"
        somatotal.innerHTML = `<p>Soma dos números selecionados é: ${somanum}</p>`

        for (Lin = 0; Lin < jogos.length;Lin++){
            for (Col = 0; Col < 15; Col++){
                for (var posX in valores){
                    verfJogo = jogos [Lin][Col] - valores [posX]
                    if (verfJogo == 0){
                        pontos++
                    }
                } 
            }
            /*Verifica */
            switch (pontos){
                case 11: ponto11++;
                        pontosTQQ [0] = ponto11
                break;
                case 12: ponto12++;
                        pontosTQQ [1] = ponto12
                break;
                case 13: ponto13++;
                        pontosTQQ [2] = ponto13
                break;
                case 14: ponto14++;
                        pontosTQQ [3] = ponto14        
                break;
                case 15: ponto15++;
                        pontosTQQ [4] = ponto15
                break;
                default:
                break;
            }
            pontos = 0
        }
        /*Concursos = jogos.length*/
        
        if (valores.length <= 14){
            confjogo.style.color = "#f00"
            confjogo.innerHTML += `<p>Selecionar menos de 15 dezenas</br></p><p>invalidam números de repetições.</br</p>`
        }else 
            confjogo.style.color = "#00f"

        if (ponto11 != 0){
            /*confjogo.style.color = "#00f"*/
            confjogo.innerHTML += `<p>11 pontos em ${ponto11} jogos.</br></p>`
        }
        if (ponto12 != 0){
            /*confjogo.style.color = "#00f"*/
            confjogo.innerHTML += `<p>12 pontos em ${ponto12} jogos.</br</p>`
        }
        if (ponto13 != 0){
            /*confjogo.style.color = "#00f"*/
            confjogo.innerHTML += `<p>13 pontos em ${ponto13} jogos.</br</p>`
        }
        if (ponto14 != 0){
            /*confjogo.style.color = "#00f"*/
            confjogo.innerHTML += `<p>14 pontos em ${ponto14} jogos.</br</p>`
        }
        if (ponto15 != 0){
            /*confjogo.style.color = "#00f"*/
            confjogo.innerHTML += `<p>15 pontos em ${ponto15} jogos.</br</p>`
        }
        ponto11 = 0
        ponto12 = 0
        ponto13 = 0
        ponto14 = 0
        ponto15 = 0
    }
} 
